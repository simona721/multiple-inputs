
#Taking two imputs at the same time
x, y = input("Enter your inputs:").split()
print(f"The input for value x is {x}")
print(f"The input for value y is {y}")

#Taking 3 values at the same time
a, b, c = input("Enter your values: ").split()
print(f"The value for a is {a}")
print(f"The value for a is {b}")
print(f"The value for a is {c}")

#Taking multiple inputs at the same time
x = [x for x in input("Enter values: ").split()]
print(f"The values are: {x}")



